# Bitwise assignment operators example
x = 15

# Right Bitwise Shift Assignment (x >>= 2 is equivalent to x = x >> 2)
x >>= 2   # Shifts bits of x two places to the right
x >>= 3  # Shifts bits of x two places to the right
print(x)  # Output: 3



# Bitwise OR Assignment (x |= 9 is equivalent to x = x | 9)
x |= 9  # Performs bitwise OR operation between x and 9
print(x)  # Output: 1